int read(){
	int s1=1,s2=0;
	char ch=getchar();
	while(!isdigit(ch)){
		if(ch=='-')
		s1=-1;
		ch=getchar();
	}
	while(isdigit(ch)){
		s2=s2*10+(ch-'0');
		ch=getchar();
	}
	return s1*s2;
}
